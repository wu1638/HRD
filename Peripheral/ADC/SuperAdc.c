//读取Light Sens的值
//0~100:0,最暗;100,最亮 
uint8_t Lsens_Get_Val(void)
{
	uint32_t temp_val=0;
	uint8_t t;
	for(t=0;t<LSENS_READ_TIMES;t++)
	{
		temp_val+=Get_Adc3();	//读取ADC值,通道6
	}
	temp_val/=LSENS_READ_TIMES;//得到平均值 
	if(temp_val>4000)temp_val=4000;
	return (uint8_t)(100-(temp_val/40));
}
//采集各路数据转换电压值显示
float Get_voltage(void)
{
	float temp;
	double adcvlotage;
	uint16_t mean;
	uint8_t i;
	
	adcvlotage=	adcbuf[10];
	adcvlotage = 1.2 /adcvlotage;
	
	for(i = 0; i < bufSize;i++)
	{
		mean += adcbuf[i];
	}
	mean = mean / bufSize;
	//temp = (float)adcbuf[id]*(3.3/4096);
	
	temp = adcvlotage * (float)mean;
	
	return temp;
}

